public class Loops {
    public static void main(String []args){
        int[] arr={3,65,23,64,1,350};
        for(int num:arr){
            System.out.println(num);
        }

        String[] names={"'array'","is","defined","here"};
        for(String name:names){
            System.out.println(name);
        }
    }
}
